#!/usr/bin/env node
// Generates src/assets/palettes.css from scripts/palettes.mjs
// Derives semantic colors and code token colors from palette primitives.
// Runs automatically during `npm run build`.

import { writeFileSync } from "node:fs";
import { oklch, formatHex, wcagContrast } from "culori";
import { palettes } from "./palettes.mjs";

// Role definitions: fixed hue targets (semantic convention)
// chroma and lightness are derived from the palette's accent
const roles = {
  "--semantic-success": { h: 140, c: 0.16 },
  "--semantic-warning": { h: 80,  c: 0.14 },
  "--semantic-danger":  { h: 25,  c: 0.18 },
  "--semantic-info":    { h: 220, c: 0.12 },
  "--code-comment":     { h: 40,  c: 0.06 },
  "--code-keyword":     { h: 320, c: 0.18 },
  "--code-symbol":      { h: 140, c: 0.16 },
  "--code-path":        { h: 220, c: 0.14 },
  "--code-status":      { h: 25,  c: 0.20 },
  "--code-string":      { h: 100, c: 0.12 },
};

function ensureContrast(hex, bgHex, target) {
  if (wcagContrast(hex, bgHex) >= target) return hex;
  const col = oklch(hex);
  const bg = oklch(bgHex);
  const darker = bg.l >= 0.5;
  let l = col.l;
  for (let i = 0; i < 200; i++) {
    l = darker ? l - 0.005 : l + 0.005;
    if (l < 0) l = 0;
    if (l > 1) l = 1;
    if (wcagContrast(formatHex({ l, c: col.c, h: col.h ?? 0, mode: "oklch" }), bgHex) >= target) {
      return formatHex({ l, c: col.c, h: col.h ?? 0, mode: "oklch" });
    }
  }
  return formatHex({ l: darker ? 0 : 1, c: col.c, h: col.h ?? 0, mode: "oklch" });
}

function deriveColors(accentHex, bgHex) {
  const accent = oklch(accentHex);
  const baseC = accent.c || 0.12;
  const bgOklch = oklch(bgHex);
  const isDarkBg = bgOklch.l < 0.5;

  const out = {};
  for (const [name, role] of Object.entries(roles)) {
    const c = Math.min(baseC * (role.c / 0.12), 0.3);
    const startL = isDarkBg ? 0.75 : 0.35;
    const hex = formatHex({ l: startL, c, h: role.h, mode: "oklch" });
    out[name] = ensureContrast(hex, bgHex, 4.5);
  }
  return out;
}

const lines = ["/* Auto-generated by scripts/generate-palettes.mjs */", ""];

for (const p of palettes) {
  lines.push(`:root[data-palette="${p.id}"] {`);
  for (const [prop, val] of Object.entries(p.light)) {
    lines.push(`  ${prop}: ${val};`);
  }
  for (const [prop, val] of Object.entries(deriveColors(p.light["--accent"], p.light["--bg"]))) {
    lines.push(`  ${prop}: ${val};`);
  }
  lines.push("}");
  lines.push("");

  lines.push(`html.dark[data-palette="${p.id}"] {`);
  for (const [prop, val] of Object.entries(p.dark)) {
    lines.push(`  ${prop}: ${val};`);
  }
  for (const [prop, val] of Object.entries(deriveColors(p.dark["--accent"], p.dark["--bg"]))) {
    lines.push(`  ${prop}: ${val};`);
  }
  lines.push("}");
  lines.push("");
}

writeFileSync("src/assets/palettes.css", lines.join("\n"));
console.log("Generated src/assets/palettes.css");
