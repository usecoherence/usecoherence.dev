#!/usr/bin/env node
// Generates src/assets/palettes.css from scripts/palettes.mjs
// Runs automatically during `npm run build`.

import { writeFileSync } from "node:fs";
import { palettes } from "./palettes.mjs";

const lines = ["/* Auto-generated by scripts/generate-palettes.mjs */", ""];

for (const p of palettes) {
  lines.push(`:root[data-palette="${p.id}"] {`);
  for (const [prop, val] of Object.entries(p.light)) {
    lines.push(`  ${prop}: ${val};`);
  }
  lines.push("}");
  lines.push("");

  lines.push(`html.dark[data-palette="${p.id}"] {`);
  for (const [prop, val] of Object.entries(p.dark)) {
    lines.push(`  ${prop}: ${val};`);
  }
  lines.push("}");
  lines.push("");
}

writeFileSync("src/assets/palettes.css", lines.join("\n"));
console.log("Generated src/assets/palettes.css");
